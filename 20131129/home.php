<script>


$(function() {

	GalleryCircus.AllMedia = $("iframe[src^='http://www.youtube.com'], iframe[src^='https://w.soundcloud.com']");
	GalleryCircus.FluidContainer = $(".media");
	GalleryCircus.Init();

});

var GalleryCircus = {

	AllMedia: null, // store all the media to be resized
	
	FluidContainer: null, // store fluid container
	
	CauseRepaintsOn: $("#contact, #innerGigContainer table, #innerGigContainerText, .media, .headerFooter"), // elements that need text repainted for Chrome
	
	Init: function() {
	
		// save all aspect ratios of media, and handler for resize window
		// then make all youtube links have 'wmode=transparent' at end of link (always display under nav)
		// apply style to nav links
		// move to furtherst polaroid
		GalleryCircus.SaveAspect();
		GalleryCircus.ReSize();
		GalleryCircus.MakeYoutubeVideosTransparent();
		GalleryCircus.ShakeIcon();
		GalleryCircus.SlideToLink();
		GalleryCircus.LinkSeeNav();
		GalleryCircus.Scroll();
		
	},
	
	SlideToLink: function() {
		// foreach a link with slide
		$('a[href*=#]').click(function() {
			if (location.pathname.replace(/^\//,'') == this.pathname.replace(/^\//,'') && location.hostname == this.hostname) {
				GalleryCircus.Slide($(this.hash));
			}
		}); 
	},
	
	Slide: function(hash) {
		// slide to div
		var $target = hash;
		$target = $target.length && $target || $('[name=' + this.hash.slice(1) +']');
		if ($target.length) {
			var targetOffset = $target.offset().top;
			$('html,body').animate({scrollTop: targetOffset - 100}, 600);
			return false;
		}
	},
	
	Scroll: function() {
		$(window).bind('scroll', function() {
			var furthestDiv = GalleryCircus.CalcPolaroidFurthestDiv();
			
			var arrowDown = "<img id=\"arrow\" src=\"/img/arrowdown.png\">";
			var arrowUp = "<img id=\"arrow\" src=\"/img/arrowup.png\">";
			
			if (furthestDiv == "polaroidsTopContainer") $("#linkSee").html(arrowUp); else $("#linkSee").html(arrowDown);
		});
	},
	
	LinkSeeNav: function() {
		$("#linkSee").click(function() {
			var furthestDiv = GalleryCircus.CalcPolaroidFurthestDiv(); 
			GalleryCircus.Slide($("#" + furthestDiv));
		});
	},
	
	CalcPolaroidFurthestDiv: function() {
		var lowestPos = null;
		var furthestDiv = "";
		$('.polaroid').each(function() {
			var polaroid = $(this);
			var position = polaroid.position().top - $(window).scrollTop();
			
			if (position < 0) position = position * -1;
			
			if (lowestPos == null || position > lowestPos)
			{
				lowestPos = position;
				furthestDiv = polaroid.attr("id");
			}
		}); 
		return furthestDiv;
	},
	
	SaveAspect: function() {
		// Figure out and save aspect ratio for each video
		GalleryCircus.AllMedia.each(function() {

			$(this)
				.data('aspectRatio', this.height / this.width)
				
				// and remove the hard coded width/height
				.removeAttr('height')
				.removeAttr('width');

		});
	},
	
	ReSize: function() {
		// When the window is resized
		// debounce this?
		$(window).resize(function() {

			GalleryCircus.FixChromeTextBugOnWindowResize();
		
			var newWidth = GalleryCircus.FluidContainer.width();
			
			// Resize all videos according to their own aspect ratio
			GalleryCircus.AllMedia.each(function() {

				var $el = $(this);
				$el
					.width(newWidth)
					.height(newWidth * $el.data('aspectRatio'));

			});

		// Kick off one resize to fix all videos on page load
		}).resize();
	},
	
	FixChromeTextBugOnWindowResize: function() {
		// re-paint (add a pointless css style) each of the following elemts to re-render viewport text size
		GalleryCircus.CauseRepaintsOn.css("border","none");
	},
	
	MakeYoutubeVideosTransparent: function() {
		$("[src^='http://www.youtube.com']").each(function() {
           var url = $(this).attr("src");
           var char = "?";
           
		   if (url.indexOf("?") != -1)
                  var char = "&";
          
            $(this).attr("src",url+char+"wmode=transparent");
		});
	},
	
	ShakeIcon: function() {
		$(".shakeable").mouseover(function () {
			$(this).effect("pulsate", { times:1 }, 500);
		});
	}
}

